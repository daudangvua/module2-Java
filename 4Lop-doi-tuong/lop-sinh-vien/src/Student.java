public class Student {
    int Id;
    String Fullname;
    String Email;
    int age;
    float Grade;

    public Student(int Id, String Fullname, String Email, int age, float Grade) {
        this.Id = Id;
        this.Fullname = Fullname;
        this.Email = Email;
        this.age = age;
        this.Grade = Grade;
    }
}

